package com.chenjun.www.operator;

public class Demo04字符连接符_扩展赋值运算 {
    public static void main(String[] args) {
        //字符串连接符  +  （如果运算中存在String类，转换成String再进行链接）
        int a = 10;
        int b = 20;
        System.out.println(""+a+b);//1020
        System.out.println(a+b+"");//30

        //扩展赋值运算符：+=    -+  *=  /=

    }
}
